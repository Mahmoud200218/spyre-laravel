// Text one
let icon_one = document.getElementById("icon-one");
let paragraph_one = document.getElementById("par-one");

icon_one.addEventListener("click", function(){
    if(paragraph_one.style.display === "none"){
        paragraph_one.style.display = "block";
    }else{
        paragraph_one.style.display = "none";
    }
})

